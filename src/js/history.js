export function generateHistoryPosts() {
  return historyPosts
    .map(
      (post) => `
            <article class="blog-post">
                <div class="period">${post.period || "Historisk periode"}</div>
                <h2>${post.title}</h2>
                <div class="content">
                    ${formatContent(post.content)}
                </div>
            </article>
        `
    )
    .join("");
}

function formatContent(content) {
  return content
    .split("\n\n")
    .map((paragraph) => paragraph.trim())
    .filter((paragraph) => paragraph.length > 0)
    .map((paragraph) => `<p>${paragraph}</p>`)
    .join("");
}
